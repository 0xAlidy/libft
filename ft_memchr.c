#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t		i;
	const char	*str;

	i = 0;
	str = s;
	while (str[i] && i < n)
	{
		if (str[i] == c)
			return ((unsigned char *)&str[i]);
		i++;
	}
	return (0);
}
