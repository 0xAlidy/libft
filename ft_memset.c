#include <string.h>

void        *ft_memset(void *b, int c, size_t len)
{
    int i;
    unsigned char *b1;

    i = 0;
    b1 = b;
    while (i < len)
    {
        b1[i] = c;
        i++;
    }
    return (b);
}
